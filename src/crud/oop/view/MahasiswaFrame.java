    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        mahasiswa = new Mahasiswa();
        mahasiswa.setNim(txtNim.getText());
        mahasiswa.setNama(txtNama.getText());
        mahasiswa.setAlamat(txtAlamat.getText());
        if(radioLk.isSelected()){
            mahasiswa.setJk("L");
        }else{
            mahasiswa.setJk("P");
        }
        mahasiswaController.save(mahasiswa);
        
        tampil();
        clearText();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void tabelDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelDataMouseClicked
        // TODO add your handling code here:
        mahasiswa = mahasiswas.get(tabelData.getSelectedRow());
        mahasiswa = mahasiswaController.getNim(mahasiswa);
        
        txtNim.setText(mahasiswa.getNim());
        txtNama.setText(mahasiswa.getNama());
        txtAlamat.setText(mahasiswa.getAlamat());
        if(mahasiswa.getJk().equals("L")){
            radioLk.setSelected(true);
        }else{
            radioPr.setSelected(true);
        }
    }//GEN-LAST:event_tabelDataMouseClicked

