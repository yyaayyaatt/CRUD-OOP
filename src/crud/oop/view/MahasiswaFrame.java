    private void tampil(){
       mahasiswas = mahasiswaController.getAll();
       Object data[][] = new Object[mahasiswas.size()][4];
       int x = 0;
        for (Mahasiswa m : mahasiswas) {
            data[x][0] = m.getNim();
            data[x][1] = m.getNama();
            data[x][2] = m.getAlamat();
            data[x][3] = m.getJk();
            ++x;
        }
        String[] header = {"NIM","Nama","Alamat","Jenis Kelamin"};
        tabelData.setModel(new DefaultTableModel(data,header));
    }

    private void clearText(){
        txtNim.setText("");
        txtNama.setText("");
        txtAlamat.setText("");
        radioLk.setSelected(true);
    }
